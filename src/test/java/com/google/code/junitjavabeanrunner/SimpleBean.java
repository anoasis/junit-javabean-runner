package com.google.code.junitjavabeanrunner;

public class SimpleBean {
	private String value;
	private int size;
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	
	public int getSize() {
		return size;
	}
	
	public void setSize(int size) {
		this.size = size;
	}
}
